<?php
   // number in lbs to convert to kgs
   $pounds = 5;
   // lbs > kgs conversion
   $poundToKg = 0.453592;
   $kilos = $pounds * $poundToKg;
   echo "<p>$pounds lbs is equal to $kilos in kgs</p>";

   // number in miles to convert kms
   $miles = 823.55;
   // miles > kms conversion
   $milesToKm = 1.60934;
   $kms = $miles * $milesToKm;
   echo "<p>$miles m is equal to $kms in kms</p>";


?>
